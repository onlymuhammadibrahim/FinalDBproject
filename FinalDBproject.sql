Use [EOT];

Create Table Supplier
(
	SupId int Primary Key,
	SupName varchar(50) Not Null,
	SupAddress varchar(255),
	SupTelephone varchar(50) Not Null,
);

Create Table Customer
(
	CusId int Primary Key,
	CusName varchar(50) Not Null,
	CusAddress varchar(255),
	CusTelephone varchar(50) Not Null,
);

Create Table Branch
(
	BraId int Primary Key,
	BraAddress varchar(255) Not Null,
);

Create Table Employee
(
	EmpId int Primary Key,
	BraId int Foreign Key References Branch(BraId),
	EmpName varchar(50) Not Null,
	EmpTelephone varchar(50) Not Null,
	EmpSalary int Not Null,
	EmpBonus int,
	EmpDesignation varchar(50) Not Null,
	EmpAddress varchar(255) Not Null,
);

Create Table Shift
(
	ShiId int Primary Key,
	EmpId int Foreign Key References Employee(EmpId),
	ShiName varchar(5) Not Null,
);

Create Table Shift_History
(
	ShiHId int Primary Key,
	ShiId int Foreign Key References Shift(ShiId),
	ShiHDateTime DateTime,
);

Create Table Equipment
(
	EquId int Primary Key,
	BraId int Foreign Key References Branch(BraId),
	EquName varchar(50) Not Null,
	EquQuantity int,
);

Create Table Section
(
	SecId int Primary Key,
	SecName varchar(50) Not Null,
);


Create Table Item
(
	IteId int Primary Key,
	SecId int Foreign Key References Section(SecId),
	IteName varchar(50) Not Null,
	IteAmount int Not Null,
	ItePrice int Not Null,
);

Create Table Item_Branch
(
	IteId int Foreign Key References Item(IteId),
	BraId int Foreign Key References Branch(BraId),
	Primary Key(IteId,BraId),
)



Create Table Cart
(
	CarId int Primary Key,
	CusId int Foreign Key References Customer(CusId),
	CarDateTime DateTime,
);


Create Table Cart_Product
(
	CarId int Foreign Key References Cart(CarId),
	IteId int Foreign Key References Item(IteId),
	CarPQuantity int Not Null,
	Primary Key(CarId,IteId),
);

Create Table Stock
(
	StoId int Primary Key,
	SupId int Foreign Key References Supplier(SupId),
	ShiId int Foreign Key References Shift(ShiId),
	StoDateTime DateTime,
);

Create Table Stock_Product
(
	StoId int Foreign Key References Stock(StoId),
	IteId int Foreign Key References Item(IteId),
	StoPQuantity int Not Null,
	StoPrice int Not Null,
	Primary Key(StoId,IteId),

);

Create Table Sale
(
	SalId int Primary Key,
	CusId int Foreign Key References Customer(CusId),
	ShiId int Foreign Key References Shift(ShiId),
	SalDateTime DateTime,
);

Create Table Sale_Product
(
	SalId int Foreign Key References Sale(SalId),
	IteId int Foreign Key References Item(IteId),
	SalPQuantity int
	Primary Key(SalId,IteId)
);

Create Table Customer_Branch
(
	CusId int Foreign Key References Customer(CusId),
	BraId int Foreign Key References Branch(BraId),
	Primary Key(CusId,BraId)
);

Create Table Review
(
	CusId int Foreign Key References Customer(CusId),
	IteId int Foreign Key References Item(IteId),
	ShiId int Foreign Key References Shift(ShiId),
	RevComments varchar(255),
	RevRating varchar(1),
	Primary Key (CusId,IteId),
);

